package com.ion.cmp.models;

public class MediaList {
    private String name;
    private String id;

    public MediaList() {}

    public void setName(String name) {
        this.name = name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }
}
